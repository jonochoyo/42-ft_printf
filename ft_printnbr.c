#include "ft_printf.h"

void	ft_printnbr(int n)
{
	unsigned int	nb;

	nb = n;
	if (n < 0)
	{
		ft_printchar('-');
		nb = n * (-1);
	}
	if (nb > 9)
	{
		ft_printnbr(nb / 10);
	}
	ft_printchar((nb % 10) + '0');
}
